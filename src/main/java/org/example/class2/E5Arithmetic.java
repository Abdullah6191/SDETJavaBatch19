package org.example.class2;

public class E5Arithmetic {
    public static void main(String[] args) {
        int num1=100;
        int num2=20;
        double num3=10.1;
        double result=num1+num2+num3;
        System.out.println(result);

        result=num1-num2-num3;
        System.out.println(result);


        result=num1*num2*num3;
        System.out.println(result);

        result=num1/num2/num3;
        System.out.println(result);








    }
}
